<template>
    <div class="hello">
        <form>
            <div class="form__group">
                <p>Email</p>
                <input type="text" v-model="email">
            </div>

            <div class="form__group">
                <p>Wachtwoord</p>
                <input type="password" v-model="password">
            </div>
            <br>

            <div class="form__group">
                <input @click.prevent="sendForm()" class="form__button" type="submit" value="Login">
            </div>

            <div v-if="send !== null" class="form__group">
                <transition enter-active-class="animated slideInUp"
                            leave-active-class="animated slideOutDown"
                            mode="out-in">
                    <p v-if="send === true">Data gestuurdüëç</p>
                    <p v-else>Data niet gestuurd üëé</p>
                </transition>
            </div>
        </form>
    </div>
</template>

<script>
    import axios from 'axios'

    export default {
        name: 'Form',
        props: {
            msg: String
        },

        data() {
            return {
                email: '',
                password: '',
                send: null,
            }
        },

        methods: {
            sendForm() {
                if (this.email !== '' || this.password !== '') {
                    axios
                        .post('https://shaif.nl/lego-toyfinder/mail/index.php', {
                            email: this.email,
                            favorites: this.password,
                        })
                        .then(response => {
                            if (response) {
                               this.send = true
                            }
                        })
                        .catch(error => {
                           this.send = false
                        })
                }
            }
        }
    }
</script>

